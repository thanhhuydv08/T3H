package swing_Object_BT5_3;

public class Film {
	String fName,fType,fDate,fCinema,fImage;


public Film(String fName, String fType, String fDate, String fCinema, String fImage) {
	super();
	this.fName = fName;
	this.fType = fType;
	this.fDate = fDate;
	this.fCinema = fCinema;
	this.fImage = fImage;
}



public String getfName() {
	return fName;
}
public void setfName(String fName) {
	this.fName = fName;
}
public String getfType() {
	return fType;
}
public void setfType(String fType) {
	this.fType = fType;
}
public String getfDate() {
	return fDate;
}
public void setfDate(String fDate) {
	this.fDate = fDate;
}
public String getfCinema() {
	return fCinema;
}
public void setfCinema(String fCinema) {
	this.fCinema = fCinema;
}
public String getfImage() {
	return fImage;
}
public void setfImage(String fImage) {
	this.fImage = fImage;
}


}
